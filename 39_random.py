import random

print("Random Integer:",random.randint(1,100))  #randint():Random Number between range
print("Random Float:",random.random())  #random():Random decimal number
list1 = [1,2,3,4,5] 
print("Random Choice From List:",random.choice(list1))  #choice():Random element from a list
'''
OUTPUT:-
Random Integer: 88
Random Float: 0.0793376697101682
Random Choice From List: 4
'''